package aula01; // 'package': Define o pacote da classe; 'aula01' é o nome do pacote

/* (Aula 01) ——— Todo programa em Java, é uma classe
 * 'public': A classe é acessível de qualquer lugar
 * 'class': Define o nome da classe
 * 'AloMundo': Nome da classe
 * 
 * 'static': O método main é um método estático
 * 'void': O método main não retorna um valor
 * 'main': O método main é o primeiro método executado na classe
 * 'String[] args': O método main recebe um array de strings como argumento
 */

public class AloMundo { // Nome da classe
    public static void main(String[] args) { // Método principal
        System.out.println("Alo, Mundo!"); // Imprime a string "Alo, Mundo!"
    }
}